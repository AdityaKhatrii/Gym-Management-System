private int getAmount(String mship){
    if(mship.isEmpty()) return 0;
    int amount = 0;
        Connection con = null;
        PreparedStatement pmt = null;
        ResultSet rs = null;
        try {

            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gms", "root", "root");
            pmt = con.prepareStatement("select charges from membership where mship_id = ?");
            pmt.setString(1, mship);
            rs = pmt.executeQuery();
            int flag = 0;
            if(rs.next())
         {
            flag++;
            amount = rs.getInt("charges");
       }
        con.close();
        pmt.close();
        rs.close();
        
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddFeesInternal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddFeesInternal.class.getName()).log(Level.SEVERE, null, ex);
        }
        // DB CONNECTION ENDED
    return amount;
}
    
    
