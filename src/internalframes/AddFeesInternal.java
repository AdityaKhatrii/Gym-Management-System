    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if(searchMidTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Member id is empty");
            return;
        }

        String mId = searchMidTxt.getText();
        String mship = mshipTxt.getText();
        String name = nameTxt.getText();
        String amount = amountTxt.getText();

        String paymentDate = sdf.format(paymentDateChoose.getDate());

        String dueDate = sdf.format(dueDateChoose.getDate());
        String paidStatus = (String) paidComboBox.getSelectedItem();
        boolean isPaid = false;
        if(paidStatus.equals("YES")){
            isPaid = true;
        }
        String fId = feesIdTxt.getText();

        try
        {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement("INSERT INTO FEES values(?,?,?,?,?,?)");
            ps.setString(1, fId);
            ps.setString(2, mId);
            ps.setString(3, amount);
            ps.setString(4, dueDate);
            ps.setBoolean(5, isPaid);
            ps.setString(6, paymentDate);

            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfully saved");
            con.close();
            ps.close();

            resetAll(false);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_saveBtnActionPerformed

